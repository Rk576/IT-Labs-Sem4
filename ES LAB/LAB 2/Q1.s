 ; PROGRAM TO ADD TEN 32 BIT NUMBERS 
 
	AREA RESET , DATA , READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode , CODE, READONLY
	ENTRY 
	EXPORT Reset_Handler
Reset_Handler
	LDR R0 , =SRC
	LDR R1 , =RES
	MOV R2, #10
	MOV R4, #0
LOOP
	LDR R3 , [R0] , #4
	ADDS R4,R4, R3
	SUBS R2, R2, #1
	BNE LOOP
	STR R4 , [R1]
STOP
	B STOP
SRC DCD 0X1, 0X2, 0X3, 0X4 , 0X5 , 0X6 , 0X7, 0X8 , 0X9 , 0X10
	AREA mydata, DATA , READWRITE
RES DCD 0
	END
