; SEARCHING ELEMENT USING LINEAR SEARCH
    AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X40001000
	DCD Reset_Handler
	ALIGN 
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =ARR
	LDR R1, =SEARCH_ELE
	LDR R1, [R1]
	MOV R2, #10
LOOP1
	LDR R3, [R0], #4
	CMP R3, R1
	BNE LOOP2
	MOV R4 , #1
	MOV R5, #0XB
	SUB R5,R2
	MOV R6, R5 ; R6 INDICATES POSITION OF SEARCHED ELEMENT
	B STOP
LOOP2
	SUBS R2, #1
	BNE LOOP1
	MOV R4, #-1
	
	
STOP
	B STOP
ARR DCD 5, 4, 6, 20, 42, 1, 2, 434, 234, 221
SEARCH_ELE DCD 434  
	END
